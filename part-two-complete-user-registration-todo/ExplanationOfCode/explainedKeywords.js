// Here's a list of the key JavaScript terms and what they do:

// if:  Starts a condition.

// else if: Checks another condition.

// const:  A variable that cannot be changed.

// function:  A block of code that does a specific task.

// e:  A variable that represents an event.

// preventDefault():  Stops an event from its default behavior.

// value:  Gets the data from an input.

// trim():  Removes extra spaces from text.

// toLowerCase():  Converts text to lowercase.

// let: A variable that can be changed.

// JSON.parse():  Converts text from JSON to JavaScript.

// localStorage:  Stores data in the browser.

// getItem():  Gets data from storage.

// some():  Checks if any item in an array matches a condition.

// =>:   Short way to write a function.

// alert:  Displays a message to the user.

// return:  Exits a function.

// push():  Adds an item to an array.

// setItem():  Saves data to storage.

// JSON.stringify():  Converts JavaScript data to JSON text.

// window:  Represents the browser window.

// location:  Gets the current web address.

// href:  The web address.

// null: Represents nothing.

// createElement():  Creates a new HTML element.

// id:  A name for an HTML element.

// className:  CSS classes for an HTML element.

// innerHTML:  The HTML inside an element.

// appendChild():  Adds an element inside another.

// forEach():  Goes through each item in an array.

// onclick:  Code that runs when an element is clicked.

// splice():  Changes an array by adding or removing items.

// find(): Returns the first element that matches.

// textContent: Sets the text of an HTML element.

// getElementById(): Finds an HTML element by its ID.